# 字符串
str = 'Comtop'
print('完整字符串：' + str)
print('字符串第一个字符：' + str[0])
print('第一个字符到倒数第二个：' + str[0:5])
print('第三个字符到第五个：' + str[2:5])
print('第三个字符到最后：' + str)
print('第一个字符到第二个：' + str)
print('输出两次字符串：' + str*2)
print(len(str))

# 反斜杠(\)+n转义特殊字符
print('hello\nrunoob')
# 字符串前加r，表示原始字符串，不转义
print(r'hello\nrunoob')
